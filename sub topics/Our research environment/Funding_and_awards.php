        <p style="padding-left:0px;">The University Research Council [URC] is pleased to announce that it has initiated a scheme to provide financial assistance up to a maximum of USD 1000.00 per year for the researchers of University of Peradeniya to facilitate publishing their research findings in high ranking peer reviewed journals.


        </p><p style="padding-left:0px;">Publishing research findings in reputed journals inevitably increases the visibility of research work and thereby it brings reputation and publicity for the researchers as well as for the university. Therefore, encouraging researchers to publish their scholarly work in reputed high impact journals is of paramount importance. Publishing in many high impact international peer reviewed journals involves page charges amounting to USD 3500 or even more, especially if the researcher intends to publish as an open access article. Publishing as an open access article invariably enhances the visibility of the research work leading to high number of citations.
          
          
          </p><p style="padding-left:0px;">Assisting researchers with their publications has been a long-felt need at Peradeniya. Providing assistance for researchers in getting their research work in high impact journals has been identified as one of the priorities in establishing the University Research Council. Accordingly, the URC takes pleasure in calling for applications from permanent employees of University of Peradeniya commencing from 01.03.2021 through 31.12.2021.
          
          
    		</p><p style="padding-left:0px;">In accordance with the mandate of the University Research Council, assisting researchers with publishing in high impact peer reviewed international journals has been identified as a priority area. Therefore, the URC proposes that researchers who intend to publish in high impact journals are provided with financial assistance as indicated below.</p>
<ul>
  <li><strong>Name of the Fund:</strong> Research Publication Facilitation Fund [RPFF]</li>
  <li><strong>Financial assistance:</strong> USD 500 per author up to a maximum of USD 1000 per paper per year.</li>
  <li><strong>Applicant:</strong> The applicant should be a permanent staff member of University of Peradeniya and one of the authors of the journal article.</li>
  <li><strong>Affiliation:</strong> University of Peradeniya should have been identified as the sole affiliation or one of the affiliations. Journal articles which do not carry University of Peradeniya as an affiliation are not eligible for funding.</li>
  <li><strong>Research work:</strong> Research should have been carried out fully or in part at University of Peradeniya. Research work carried out by a permanent staff member of University of Peradeniya completely outside of University of Peradeniya may also be eligible.</li>
  <li><strong>Journal:</strong> The journal should be indexed in SCI/SSCI or SCI-expanded/SSCI-expanded journal with a journal impact of 2 or higher.</li>
  <li><strong>Frequency:</strong> Once a calendar year.</li>
  <li><strong>APC Procedure:</strong> The applicant should fill in an application form and submit the same to the Director, URC through the Head of the Department and the Dean of the Faculty along with the original copy of the receipt for the payment of page charges and a copy of the journal article. The article should have been published either online or printed with a valid DOI in the journal within the year of concern. Upon receiving the approval for the application of Research Publication Facilitation from URC, through
an email, submit the invoice for the payment of the Research Publication, and the credit card statement
to process the payment.</li>
  <li><strong>The use of Institutional e-mail address:</strong> The author/s affiliated to University of Peradeniya are encouraged to use the official e-mail address with pdn domain name provided by University of Peradeniya in correspondence with the journal. If the applicant is the corresponding author of the journal article the author should have used the institutional e-mail address.</li>
  <li><strong>Others:</strong> The URC reserves the right to amend the selection criteria. The article processing charge will be reimbursed subject to the availability of funds.</li>
